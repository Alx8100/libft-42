#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ptr;
	int		size;
	int		i;

	size = ft_strlen((char *)s);
	ptr = (char *)malloc(sizeof(char) * (size + 1));
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (i < size)
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
